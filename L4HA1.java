package l4ha1;

public class L4HA1 {

    private int marks1;
    private int marks2;
    private int marks3;

    public L4HA1() {
        marks1 = 10;
        marks2 = 60;
        marks3 = 2;
    }

    public L4HA1(int m1, int m2, int m3) {

        marks1 = m1;
        marks2 = m2;
        marks3 = m3;
    }

    public void setMarks(int m1) {
        marks1 = m1;
    }

    public void setMarks2(int m2) {
        marks2 = m2;
    }

    public void setMarks3(int m3) {
        marks3 = m3;
    }

    public int getmarks1() {
        return marks1;
    }

    public int getmarks2() {
        return marks2;
    }

    public int getmarks3() {
        return marks3;
    }
}


      
    
    

